package test;

public class Line_Points
{
	int x, y1, y2;
	public  Line_Points()
	{
		super();
	}
	
	public Line_Points(int i,int j,int k)
	{
		this.x=i;
		this.y1=j;
		this.y2=k;
	}

	

	public int getX()
	{
		return x;
	}

	public int getY1()
	{
		return y1;
	}

	public int getY2()
	{
		return y2;
	}

}
